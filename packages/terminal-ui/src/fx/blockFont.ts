export const BLOCK_FONT_HEIGHT = 5;

const FONT: Record<string, string[]> = {};
for (const [ch, rows] of Object.entries(RAW)) {
    FONT[ch] = rows.map((r) => r.replace(/#/g, '█').replace(/\./g, ' '));
}

/**
 * Render text in the block font: 5 equal-width rows of '█'/space. Input is
 * uppercased; unsupported characters render as a space glyph. Glyphs are
 * separated by a one-column gap.
 */
export function renderBlock(text: string): string[] {
    const rows: string[] = ['', '', '', '', ''];
    const chars = [...text.toUpperCase()];
    for (let c = 0; c < chars.length; c++) {
        const glyph = FONT[chars[c]] ?? FONT[' '];
        for (let r = 0; r < BLOCK_FONT_HEIGHT; r++) {
            rows[r] += (c > 0 ? ' ' : '') + glyph[r];
        }
    }
    return rows;
}
